    public void logar(){
        String sql = "SELECT * FROM usuarios WHERE email = ? AND senha = ?";
        
        try{
            //capturar o que o usuario digitar na caixa de txtUsuario e txtSenha
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            pst.setString(2, txtSenha.getText());
            //a linha abaixo executa a consulta
            rs = pst.executeQuery();
            //se existir usuario e senha correspondente
            if(rs.next()){
                
                TelaPrincipal principal = new TelaPrincipal();
                principal.setVisible(true);
                TelaPrincipal.lbl_usuario.setText(rs.getString(2));
                this.dispose();
                //é um método que implementa o código necessário para finalizar o objeto e liberar a memória.
                conexao.close();
            }
            else{
               JOptionPane.showMessageDialog(null, "Usuario/Senha INVÁLIDOS");
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

